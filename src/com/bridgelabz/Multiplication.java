// Multiplication Table
package com.bridgelabz;

public class Multiplication
{
    public static void main(String[] args)
    {
        System.out.println("Multiplication table");
        for (int i=1; i<=10; i++)
        {
            int sum = 2*i;
            System.out.println("2X"+i+"="+sum);
        }
    }
}
