// Print number 100 to 1
package com.bridgelabz;

public class ForLoop100to1
{
    public static void main(String[] args)
    {
        int count = 0;
    for (int i = 100; i >= 1; i--)
    {
        System.out.print(i + " ");
        count++;
    }
       System.out.println("\nCount is : "+count);
    }
}
